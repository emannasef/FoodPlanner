package eg.gov.iti.jets.mad.foodplanner.Model;

public class FireMeal {
    String mealName;
    String mealImage;
    String mealCountryName;
    String mealId;

    public String getMealId() {
        return mealId;
    }

    public void setMealId(String mealId) {
        this.mealId = mealId;
    }

    public String getMealName() {
        return mealName;
    }

    public void setMealName(String mealName) {
        this.mealName = mealName;
    }

    public String getMealImage() {
        return mealImage;
    }

    public void setMealImage(String mealImage) {
        this.mealImage = mealImage;
    }

    public String getMealCountryName() {
        return mealCountryName;
    }

    public void setMealCountryName(String mealCountryName) {
        this.mealCountryName = mealCountryName;
    }
}
